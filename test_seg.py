def test(args, io):
    dataset = MiniChallenge("data/MiniChallenge/", args.num_points, partition='test', radius=args.radius)    
    test_loader = DataLoader(dataset, num_workers=6,
                              batch_size=args.batch_size, shuffle=False, drop_last=False)
   
    
    device = torch.device("cuda" if args.cuda else "cpu")

    #Try to load models
    model = DGCNNSeg(args).to(device)
    model = nn.DataParallel(model)
    model.load_state_dict(torch.load(args.model_path))
    model = model.eval()
    count = 0.0
    for i in tqdm(range(args.epochs)):
        for data, indices in test_loader:
            with torch.no_grad():
                data = data.to(device)
                data = data.permute(0, 2, 1)
                logits = model(data)
                preds = logits.argmax(dim=1).detach().cpu().numpy()
                dataset.update_labels(indices, preds)
            
    dataset.write_cloud("checkpoints/cloud.ply")


